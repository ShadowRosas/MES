import sys

# FUNCTIONS

while True:
    line = sys.stdin.readline().strip()

    if line == "1":
        print('uno')
        exit(0)
    elif line == "2":
        print('dos')
        exit(0)
    elif line == "3":
        print('tres')
        exit(0)
    elif line == "4":
        print('cuatro')
        exit(0)
    elif line == "5":
        print('cinco')
        exit(0)
